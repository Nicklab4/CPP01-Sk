#include <tchar.h>
#include <iostream>

int main() {
	std::system("chcp 1251");
	int celsMin, celsMax, step, count;
	float fahr;

	std::cout << "Ввод:\n";
	std::cout << "Нижняя граница: ";
	std::cin >> celsMin;
	std::cout << "Верхняя граница: ";
	std::cin >> celsMax;
	std::cout << "Шаг: ";
	std::cin >> step;

	std::cout << "Вывод:\n";
	std::cout << "\tC\tF\n";

	if ((celsMax - celsMin) % step ==0 )
		count = (celsMax - celsMin) / step;
	else
		count = (celsMax - celsMin) / step + 1;

	for(int i = 0; i <= count; i++ )
		std::cout << "\t" << i * step << "\t" << (int)(32 + (i * step) * 1.8) << "\n";
}