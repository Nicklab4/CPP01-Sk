//должна печататься, даже если последний шаг «перепрыгнул» её. Известно, что 0 °С
//соответствует 32 °F, а каждый градус Цельсия эквивалентен 1,8 градусам Фаренгейта. 
//
//Пример
//Ввод:
//Нижняя граница: 0.
