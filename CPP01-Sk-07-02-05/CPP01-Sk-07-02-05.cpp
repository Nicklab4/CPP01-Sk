//которое тот выдавал вчера, и продолжает это делать до тех пор, пока пользователь
//не ответит ему “Да, конечно, сделал”. Для большей реалистичности в конце пусть
//робот-начальник пожалуется: “Ну почему тебя нужно спрашивать N раз?”, где N —
//это число попыток, которые были потрачены пользователем, прежде чем он ввёл
