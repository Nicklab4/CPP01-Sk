#include <tchar.h>
#include <iostream>
#include <string>

int main() {
	std::system("chcp 1251");
	std::string str;

	std::cout << "Вы должны ответить \"Да, конечно, сделал.\"\n\n";

	for(int i = 1; i > 0; i++){
		std::cout << "- Добрый день, выполнил ли ты задания, которые давались вчера?\n- ";
		std::getline(std::cin, str);

		if(str == "Да, конечно, сделал."){
			std::cout << "- Молодец! Но почему тебя нужно спрашивать " << i << " раз?\n";
			break;
		}
	}
}